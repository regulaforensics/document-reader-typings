/**
* Enumeration contains a set of constants specifying the type of the RFID-chip
* by the physical parameters of connection between antennas of the chip and the reader
* @enum {number}
*/
export enum eRfidType {
  /**
  * Unknown
  */
  UNKNOWN = 0,

  /**
  * Type «A»
  */
  A = 1,

  /**
  * Type «B»
  */
  B = 2,
}
